     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $venta = new Venta();
        $venta->precioTotal = $request->get('precioTotal');
        $venta->modoPedido = $request->get('modoPedido');
        $venta->formaPago = $request->get('formaPago');
        $venta->estado = $request->get('estado');
        $venta->id_cliente = $request->get('id_cliente');
        $venta->id_user = $request->get('id_user');

        $venta->save();
    }

